def random_cells(nx, ny, p_life):
    r = np.random.rand(nx, ny)
    cells = np.zeros_like(r, dtype=int)
    for i in range(nx):
        for j in range(ny):
            if r[i,j] <= p_life:
                cells[i,j] = 1
    return cells
